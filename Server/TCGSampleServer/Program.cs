using System;
using System.Diagnostics;
using System.Reflection;
using System.Security.Principal;

namespace TCGSampleServer
{
    class Program
    {
        static void Main(string[] args)
        {
            if (IsAdministrator() == false)
            {
                try
                {
                    ProcessStartInfo procInfo = new ProcessStartInfo();
                    procInfo.UseShellExecute = true;
                    procInfo.FileName = Assembly.GetEntryAssembly().Location;
                    procInfo.WorkingDirectory = Environment.CurrentDirectory;
                    procInfo.Verb = "runas";
                    Process.Start(procInfo);
                }
                catch (Exception ex)
                {
                    System.Console.WriteLine(ex.Message.ToString());
                    return;
                }
            }
            else
            {
                string baseDir = AppDomain.CurrentDomain.BaseDirectory;
                NLog.LogManager.Configuration = new NLog.Config.XmlLoggingConfiguration(baseDir + @"../Config/NLog.config", true);

                AppMain am = new AppMain();
                am.Run(args);
            }
        }

        public static bool IsAdministrator()
        {
            WindowsIdentity identity = WindowsIdentity.GetCurrent();

            if (null != identity)
            {
                WindowsPrincipal principal = new WindowsPrincipal(identity);
                return principal.IsInRole(WindowsBuiltInRole.Administrator);
            }

            return false;
        }
    }
}